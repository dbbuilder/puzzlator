<script setup lang="ts">
import { ref, computed } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '@/stores/auth'
import { useAppStore } from '@/stores/app'

const router = useRouter()
const authStore = useAuthStore()
const appStore = useAppStore()

// Form data
const username = ref('')
const email = ref('')
const password = ref('')
const acceptTerms = ref(false)

// Validation
const usernameError = ref('')
const passwordStrength = ref(0)

// Computed
const passwordStrengthText = computed(() => {
  if (password.value.length === 0) return ''
  if (passwordStrength.value < 2) return 'Weak password'
  if (passwordStrength.value < 3) return 'Fair password'
  if (passwordStrength.value < 4) return 'Good password'
  return 'Strong password'
})

// Methods
function checkUsername() {
  const value = username.value
  if (value.length < 3) {
    usernameError.value = 'Username must be at least 3 characters'
  } else if (!/^[a-zA-Z0-9_-]+$/.test(value)) {
    usernameError.value = 'Only letters, numbers, underscores, and hyphens allowed'
  } else {
    usernameError.value = ''
  }
}

function checkPasswordStrength() {
  let strength = 0
  const value = password.value
  
  if (value.length >= 6) strength++
  if (value.length >= 10) strength++
  if (/[A-Z]/.test(value) && /[a-z]/.test(value)) strength++
  if (/[0-9]/.test(value) && /[^A-Za-z0-9]/.test(value)) strength++
  
  passwordStrength.value = strength
}

function getStrengthBarClass(index: number) {
  if (password.value.length === 0) return 'bg-game-border'
  if (index <= passwordStrength.value) {
    if (passwordStrength.value < 2) return 'bg-red-500'
    if (passwordStrength.value < 3) return 'bg-yellow-500'
    if (passwordStrength.value < 4) return 'bg-blue-500'
    return 'bg-green-500'
  }
  return 'bg-game-border'
}

function getStrengthTextClass() {
  if (passwordStrength.value < 2) return 'text-red-500'
  if (passwordStrength.value < 3) return 'text-yellow-500'
  if (passwordStrength.value < 4) return 'text-blue-500'
  return 'text-green-500'
}

async function handleSubmit() {
  if (usernameError.value) {
    appStore.showError('Please fix the username error')
    return
  }
  
  const { success } = await authStore.signUp(email.value, password.value, username.value)
  if (success) {
    router.push('/')
  }
}

async function handleGoogleAuth() {
  appStore.showInfo('Google authentication coming soon!')
}

async function handleGithubAuth() {
  appStore.showInfo('GitHub authentication coming soon!')
}

function showTerms() {
  appStore.showInfo('Terms of Service coming soon!')
}

function showPrivacy() {
  appStore.showInfo('Privacy Policy coming soon!')
}
</script>

